def Math1={
	var i:Int=2;
	var k:Int=2;
	var j:Int=2;
	var m:Int=5;
	var n:Int=5;
	var f=12.0F;
	var g=4.0F;
	var c:Char='X';
	
	val A:Int=k+12*m;
	val B:Double=m/j;
	val C:Int=n%j;
	val D:Int=m/j*j;
	val E:Double=f+10*5+g;
	val F:Double=1+i*n;
	
	println("k+12*m=" + A);
	println("m/j="+B);
	println("n%j="+C);
	println("m/j*j="+ D);
	println("f+10*5+g="+E);
	println("++i*n="+F);
	
}


